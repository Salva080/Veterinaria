   private void armaCabeceraTabla() {

        //Titulos de Columnas
        ArrayList<Object> columnas = new ArrayList<Object>();
        columnas.add("ID");
        columnas.add("DNI");
        columnas.add("Apellido");
        columnas.add("Nombre");
        columnas.add("Direccion");
        columnas.add("Telefono");
        columnas.add("Contacto");

        for (Object it : columnas) {

            modelo.addColumn(it);
        }
        tClientes.setModel(modelo);
    }
    private void borraFilasTabla() {

        int a = modelo.getRowCount() - 1;

        for (int i = a; i >= 0; i--) {

            modelo.removeRow(i);
        }
    }
    private void cargarDatosActivos(){
        borraFilasTabla();
        ArrayList<Cliente> lista = (ArrayList) clienteData.listarClienteActivos();

        for (Cliente a : lista) {

            modelo.addRow(new Object[]{a.getIdCliente(),a.getDni(), a.getApellido(), a.getNombre(), a.getDireccion(), a.getContactoAlternativo()});
        }
    }
    private void cargaDatosInactivos() {

        borraFilasTabla();
        //Llenar filas con las materias en las que esta incripto un alumno

        ArrayList<Cliente> lista = (ArrayList) clienteData.listarClientesInactivos();

        for (Cliente a : lista) {

            modelo.addRow(new Object[]{a.getIdCliente(),a.getDni(), a.getApellido(), a.getNombre(), a.getDireccion(), a.getContactoAlternativo()});
        }
    }
    

